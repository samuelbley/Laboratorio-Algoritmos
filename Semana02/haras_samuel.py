# Um haras necessita trocar todas as ferraduras dos cavalos. Sabe-se que o valor de cada ferradura 
# custa R$80,00. Para isso, faça um algoritmo que solicite o número de cavalos e apresente o valor total 

cavalos = int(input("Quantos cavalos você tem? "))
print("Você gastará", ((cavalos * 4) * 80), "reais caso troque as 4 ferraduras!")

