#4 - Uma empresa deseja trocar os pneus de toda frota de carros.
#Sabe-se que cada pneu custa R$395,40. Com isso, leia o total de carros e apresente o valor final.
#Utilize a função input.

carros = float(input("Quantos carros possui essa empresa?"))
val_final = (carros * 4) * 395.40
print("Essa empresa gartará",val_final,"reais.")
